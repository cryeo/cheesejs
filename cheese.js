    Object.prototype.extend = function(obj) {
        if (!obj) {
            obj = {};
        }
        var that = this;
        obj.forEach(function(key, value, index) {
            if (obj.hasOwnProperty(key)) {
                that[key] = obj[key];
            }
        })
    };
